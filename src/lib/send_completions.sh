## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# itkdev-test-site completion                              -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/bashly-framework/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_itkdev-test-site_completions_filter() {'
  echo $'  local words="$1"'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local result=()'
  echo $''
  echo $'  # words the user already typed (excluding the command itself)'
  echo $'  local used=()'
  echo $'  if ((COMP_CWORD > 1)); then'
  echo $'    used=("${COMP_WORDS[@]:1:$((COMP_CWORD - 1))}")'
  echo $'  fi'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    # Completing an option: offer everything (including options)'
  echo $'    echo "$words"'
  echo $''
  echo $'  else'
  echo $'    # Completing a non-option: offer only non-options,'
  echo $'    # and don\'t re-offer ones already used earlier in the line.'
  echo $'    for word in $words; do'
  echo $'      [[ "${word:0:1}" == "-" ]] && continue'
  echo $''
  echo $'      local seen=0'
  echo $'      for u in "${used[@]}"; do'
  echo $'        if [[ "$u" == "$word" ]]; then'
  echo $'          seen=1'
  echo $'          break'
  echo $'        fi'
  echo $'      done'
  echo $'      ((!seen)) && result+=("$word")'
  echo $'    done'
  echo $''
  echo $'    echo "${result[*]}"'
  echo $'  fi'
  echo $'}'
  echo $''
  echo $'_itkdev-test-site_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compwords=()'
  echo $'  if ((COMP_CWORD > 0)); then'
  echo $'    compwords=("${COMP_WORDS[@]:1:$((COMP_CWORD - 1))}")'
  echo $'  fi'
  echo $'  local compline="${compwords[*]}"'
  echo $''
  echo $'  COMPREPLY=()'
  echo $''
  echo $'  case "$compline" in'
  echo $'    *\'config unset\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help -h compose_command compose_command_config_filename compose_files site_base_dir")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'config show\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'config set\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help -h compose_command compose_command_config_filename compose_files site_base_dir site_base_domain")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'install\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help --install -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'update\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help --install -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'delete\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help --yes -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'config\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help -h set show unset")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'purge\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--created-before --dry-run --help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'list\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--help --quiet --show-compose-config --show-compose-containers --verbose -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_itkdev-test-site_completions_filter "--debug --help --verbose --version -h -v config delete install list purge update")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'  complete -F _itkdev-test-site_completions itkdev-test-site'
  echo $''
  echo $'# ex: filetype=sh'
}